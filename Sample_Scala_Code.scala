/**Scala program to find a number is positive, negative or positive.*/

    object excheckNumber {
       def main(args: Array[String]) {
        
            /**declare a variable*/
            var Number= (-100);
       
           if(Number==0){
               println("Number is zero")
           }
           else if(Number>0){
               println("Number is positive")
           }
           else{
               println("Number is negative")
           }
       }
    }